/**
 * Project-wide code intelligence for JavaScript/TypeScript - on by default.
 *
 * vtsls (tsserver) only analyses your *whole* project - cross-file Find Usages, rename,
 * go-to-definition into other files - when it can find an on-disk `tsconfig.json`/`jsconfig.json`.
 * Without one it falls back to an "inferred project" scoped to the open file and its imports.
 *
 * Rather than nag with a dialog, we make project-wide intelligence the default: the first time a
 * JS/TS file is opened in a project that has no config, we silently create one - `jsconfig.json`
 * for JS projects, `tsconfig.json` (with allowJs + noEmit, so it stays build-safe) when the project
 * already contains TypeScript. The generated file self-documents through an
 * `autoGeneratedByPhoenixCode` marker whose `autoManage: true` is the contract that lets Phoenix
 * keep managing the file silently - e.g. upgrading jsconfig -> tsconfig when TypeScript is later
 * added to the project - always preserving the user's compilerOptions. Setting `autoManage: false`
 * (or hand-creating a config) makes the file user-owned: Phoenix never touches it again. To opt out
 * entirely, the user deletes the config; we remember (PREF_CREATED) and won't recreate it - the
 * Problems panel offers a one-click re-enable (the only flow that still shows a toast, as feedback
 * for an explicit click).
 *
 * Desktop-only (the LSP is desktop-only); never runs in test windows (it would write configs into
 * fixtures and break tests).
 *
 * @module extensionsIntegrated/TypeScriptSupport/CodeIntelligence
 */
define(function (require, exports, module) {


    const ProjectManager = brackets.getModule("project/ProjectManager"),
        EditorManager = brackets.getModule("editor/EditorManager"),
        MainViewManager = brackets.getModule("view/MainViewManager"),
        FileSystem = brackets.getModule("filesystem/FileSystem"),
        CommandManager = brackets.getModule("command/CommandManager"),
        Commands = brackets.getModule("command/Commands"),
        NotificationUI = brackets.getModule("widgets/NotificationUI"),
        NativeApp = brackets.getModule("utils/NativeApp"),
        PreferencesManager = brackets.getModule("preferences/PreferencesManager"),
        StringUtils = brackets.getModule("utils/StringUtils"),
        Strings = brackets.getModule("strings");

    // Per-project persisted flag: Phoenix has created a config here at least once. We never
    // auto-create again once set, so deleting the config (the opt-out) sticks; the Problems-panel
    // row then offers to re-enable.
    const PREF_CREATED = "tsCodeIntel.created";

    // Global user preference: set false to stop auto-creating configs in new projects entirely
    // (surfaced both in the preferences file and as a checkbox in the config settings panel).
    const PREF_AUTO_CREATE = "codeIntel.autoCreateConfig";
    PreferencesManager.definePreference(PREF_AUTO_CREATE, "boolean", true, {
        description: Strings.DESCRIPTION_CODE_INTEL_AUTO_CREATE
    });

    // "Learn more" -> the TypeScript/JavaScript config reference (documents every compilerOption in
    // the jsconfig.json we generate: module, target, moduleResolution, checkJs, jsx, ...).
    const DOCS_URL = "https://www.typescriptlang.org/tsconfig/";

    // The configs we create: jsconfig for JS projects (editor-only, can never change a build),
    // tsconfig once the project contains TypeScript (external TS tooling only reads tsconfig).
    const JS_CONFIG_FILE = "jsconfig.json";
    const TS_CONFIG_FILE = "tsconfig.json";
    // Configs whose presence means the project is already scoped - leave it alone.
    const EXISTING_CONFIG_FILES = [TS_CONFIG_FILE, JS_CONFIG_FILE];

    // Languages that map to a "TypeScript" label.
    const TS_LANGUAGES = ["typescript", "tsx"];

    // The self-documenting marker embedded (first, so it's the first thing a user opening the file
    // reads) in every config we generate. autoManage is the management contract: while true, Phoenix
    // may silently rewrite/upgrade the file (always preserving compilerOptions); the user sets it to
    // false to take ownership, after which Phoenix never touches the file again. TypeScript ignores
    // unknown top-level keys in ts/jsconfig (custom fields like ts-node's are established practice).
    function _marker() {
        return {
            doc: "Phoenix Code created this file to enable advanced code intelligence for your " +
                "project. Delete it to turn that off.",
            autoManage: true,
            autoManageDoc: "While autoManage is true, Phoenix Code keeps this file up to date " +
                "automatically (e.g. upgrades jsconfig.json to tsconfig.json when TypeScript " +
                "files are added), preserving your compilerOptions. Set autoManage to false to " +
                "stop Phoenix Code from ever modifying this file."
        };
    }

    // One UNIVERSAL, permissive template - deliberately not a per-flavor detection matrix. It has
    // to serve editor intelligence (never builds) across every JS flavor a project may use:
    // - module "preserve" (TS 5.4+): resolves BOTH `import` and `require()` and never demands file
    //   extensions on relative imports (nodenext would, breaking go-to-def in extensionless ESM JS);
    //   implies bundler moduleResolution + esModuleInterop + resolveJsonModule. Per-file CJS-vs-ESM
    //   classification stays driven by file extension + nearest package.json "type" regardless - so
    //   Node-CJS, Node-ESM, bundler and browser projects all resolve under this one setting.
    // - target "esnext" with no `lib` includes DOM by default -> browser globals just work.
    // - jsx "react-jsx": modern automatic runtime, no "Cannot find name 'React'" friction.
    // - allowUmdGlobalAccess: module files may reference UMD/browser globals (jQuery-style) without
    //   imports - an intelligence win for browser/UMD projects, acceptable looseness for an
    //   editor-only, checkJs:false-by-default config.
    // - typeAcquisition.enable: THE critical line. ATA (tsserver auto-fetching @types/node and types
    //   for package.json deps via npm, even with no node_modules) defaults ON for jsconfig but OFF
    //   for tsconfig - without this, the jsconfig->tsconfig upgrade would silently kill Node
    //   builtin/require("pkg") IntelliSense. Explicit on both file types; degrades gracefully to
    //   locally installed @types when npm/network are unavailable.
    // Known limitation: tsserver cannot infer AMD/RequireJS define() modules cross-file (no config
    // fixes that); users can still add their own paths/baseUrl, which the preserve-merge keeps.
    // compilerOptions from an existing managed config being rewritten/upgraded are preserved over
    // the defaults; `checkJs` is applied only when the caller passes it, else the preserved value
    // stands. tsconfig.json - unlike editor-only jsconfig - is read by real build tools, so it
    // additionally gets allowJs (JS files stay first-class in mixed projects) and noEmit (an
    // accidental `npx tsc` must never emit files next to sources).
    function _configContent(fileName, existingCompilerOptions, checkJs) {
        const compilerOptions = Object.assign({
            module: "preserve",
            target: "esnext",
            checkJs: false,
            jsx: "react-jsx",
            allowUmdGlobalAccess: true
        }, existingCompilerOptions || {});
        if (checkJs !== undefined) {
            compilerOptions.checkJs = !!checkJs;
        }
        if (fileName === TS_CONFIG_FILE) {
            compilerOptions.allowJs = true;
            compilerOptions.noEmit = true;
        }
        return {
            autoGeneratedByPhoenixCode: _marker(),
            compilerOptions: compilerOptions,
            typeAcquisition: { enable: true },
            exclude: ["node_modules", "bower_components", "dist", "build"]
        };
    }

    // Strip JSONC comments and trailing commas so a config can be JSON.parse'd (same tolerance
    // main.js uses for checkJs detection). Good enough for our own + lightly-edited configs; a file
    // this can't parse is treated as user-owned and never touched.
    function _stripJsonComments(str) {
        str = str || "";
        str = str.replace(/\/\*(?:(?!\*\/)[\s\S])*\*\//g, "");  // block comments
        str = str.replace(/\/\/[^\n\r]*/g, "");                 // line comments
        str = str.replace(/,(\s*[}\]])/g, "$1");                // trailing commas
        return str;
    }

    // Read + tolerant-parse one config file; resolves null when missing/unreadable/unparseable.
    function _readConfigJson(rootPath, fileName) {
        return new Promise(function (resolve) {
            FileSystem.getFileForPath(rootPath + fileName).read(function (err, content) {
                if (err || !content) {
                    resolve(null);
                    return;
                }
                try {
                    resolve(JSON.parse(_stripJsonComments(content)));
                } catch (e) {
                    resolve(null);
                }
            });
        });
    }

    /**
     * The config Phoenix is allowed to manage: the first of tsconfig/jsconfig that parses AND
     * carries autoGeneratedByPhoenixCode.autoManage === true. Everything else (hand-written configs,
     * autoManage:false, unparseable files, older Phoenix formats without the flag) is user-owned -
     * never returned here, never modified.
     * @param {string} rootPath
     * @return {Promise<?{fileName:string, json:Object}>}
     */
    async function _readManagedConfig(rootPath) {
        for (const fileName of EXISTING_CONFIG_FILES) {
            const json = await _readConfigJson(rootPath, fileName);
            const marker = json && json.autoGeneratedByPhoenixCode;
            if (marker && marker.autoManage === true) {
                return { fileName: fileName, json: json };
            }
        }
        return null;
    }

    // Options injected by main.js (kept decoupled from the LSP client wiring).
    let _options = {};
    // Project roots we've already evaluated this session (avoids re-scanning on every file switch).
    const _evaluated = new Set();
    // Project roots where the user closed the Problems-panel re-enable row this session.
    const _panelRowClosed = new Set();
    // The currently-shown toast, so we can dismiss it when the project changes.
    let _activeNotification = null;

    function _dismissActiveNotification() {
        if (_activeNotification) {
            _activeNotification.close();
            _activeNotification = null;
        }
    }

    function _projectRootPath() {
        const root = ProjectManager.getProjectRoot();
        return root && root.fullPath;
    }

    // True if the active project changed since `rootPath` was captured - i.e. the user switched
    // projects while an await was in flight, so the in-progress result is now stale. (This re-reads
    // the *current* root on purpose; comparing against the captured `rootPath` is the whole point.)
    function _projectChangedSince(rootPath) {
        return _projectRootPath() !== rootPath;
    }

    function _isCreated() {
        return !!PreferencesManager.stateManager.get(PREF_CREATED, PreferencesManager.stateManager.PROJECT_CONTEXT);
    }

    function _setCreated(val) {
        PreferencesManager.stateManager.set(PREF_CREATED, !!val, PreferencesManager.stateManager.PROJECT_CONTEXT);
    }

    /**
     * @return {Promise<boolean>} true if the project root already has a tsconfig/jsconfig.
     */
    function _hasProjectConfig() {
        const rootPath = _projectRootPath();
        if (!rootPath) {
            return Promise.resolve(false);
        }
        return Promise.all(EXISTING_CONFIG_FILES.map(function (name) {
            return new Promise(function (resolve) {
                FileSystem.getFileForPath(rootPath + name).exists(function (err, exists) {
                    resolve(!err && exists);
                });
            });
        })).then(function (results) {
            return results.indexOf(true) !== -1;
        });
    }

    /**
     * @return {Promise<boolean>} true if the project contains a real .ts/.tsx file. `.d.ts` ambient
     * declarations don't count - they're common in plain-JS projects and don't make it a TS project.
     * Only TypeScript is probed: a project with no .ts is labelled JavaScript by default, so there's
     * nothing to gain from a separate JS scan.
     */
    function _projectHasTsFiles() {
        return new Promise(function (resolve) {
            // getAllFiles returns the project's cached file list; `.some()` stops at the first match
            // instead of collecting every TS file just to check the count.
            ProjectManager.getAllFiles().done(function (files) {
                resolve(files.some(function (file) {
                    return (/\.tsx?$/i).test(file.name) && !(/\.d\.ts$/i).test(file.name);
                }));
            }).fail(function () {
                resolve(false);
            });
        });
    }

    function _fileExists(fullPath) {
        return new Promise(function (resolve) {
            FileSystem.getFileForPath(fullPath).exists(function (err, exists) {
                resolve(!err && exists);
            });
        });
    }

    // Open the project's config in the editor: the managed one if any, else whichever exists.
    async function _openConfig() {
        const rootPath = _projectRootPath();
        if (!rootPath) {
            return;
        }
        const managed = await _readManagedConfig(rootPath);
        let fileName = managed && managed.fileName;
        if (!fileName) {
            for (const name of EXISTING_CONFIG_FILES) {
                if (await _fileExists(rootPath + name)) {
                    fileName = name;
                    break;
                }
            }
        }
        if (fileName && !_projectChangedSince(rootPath)) {
            CommandManager.execute(Commands.FILE_OPEN, { fullPath: rootPath + fileName });
        }
    }

    /**
     * Write (create or rewrite) the managed config, preserving the compilerOptions of an existing
     * managed config (the autoManage contract), then restart the server so it re-scopes.
     * @param {string} fileName - JS_CONFIG_FILE or TS_CONFIG_FILE
     * @param {boolean} [checkJs] - set checkJs explicitly; omit to preserve the existing value
     * @return {Promise<boolean>} resolves true on success
     */
    async function _writeConfig(fileName, checkJs) {
        const rootPath = _projectRootPath();
        if (!rootPath) {
            return false;
        }
        const managed = await _readManagedConfig(rootPath);
        if (_projectChangedSince(rootPath)) {
            return false;
        }
        const existingOptions = managed && managed.json.compilerOptions;
        const content = JSON.stringify(_configContent(fileName, existingOptions, checkJs), null, 4) + "\n";
        return new Promise(function (resolve) {
            FileSystem.getFileForPath(rootPath + fileName).write(content, function (err) {
                if (err) {
                    console.error("[TypeScriptSupport] failed to write " + fileName, err);
                    resolve(false);
                    return;
                }
                _setCreated(true); // remember we own a config here, so deletion = opt-out
                if (typeof _options.restartServer === "function") {
                    _options.restartServer();
                }
                resolve(true);
            });
        });
    }

    function _learnMore() {
        NativeApp.openURLInDefaultBrowser(DOCS_URL);
    }

    // Reusable quiet/subtle NotificationUI surface (theme-matching, not a bright colored toast).
    const TOAST_STYLE = NotificationUI.NOTIFICATION_STYLES_CSS_CLASS.SUBTLE;

    // Show this toast and remember it so a project switch can dismiss it.
    function _trackToast(title, $tpl) {
        _dismissActiveNotification();
        const notification = NotificationUI.createToastFromTemplate(title, $tpl,
            // instantOpen: skip the default ~2s open-animation delay so it shows right away.
            { dismissOnClick: false, toastStyle: TOAST_STYLE, autoCloseTimeS: 30, instantOpen: true });
        _activeNotification = notification;
        notification.done(function () {
            if (_activeNotification === notification) {
                _activeNotification = null;
            }
        });
        return notification;
    }

    function _action(label) {
        return $("<button class='ts-code-intel-action'>").text(label);
    }

    /**
     * Unobtrusive "intelligence enabled" toast. For a plain-JS project it also offers a one-click
     * "Enable TypeScript" (turns on TypeScript-grade type checking via checkJs). No "Undo" - to opt
     * out the user just opens the config (See Config) and deletes it.
     * @param {boolean} isTs - label as TypeScript (and hide the "enable TS" button)
     */
    function _showEnabledToast(isTs) {
        const langName = isTs ? Strings.CODE_INTEL_TS : Strings.CODE_INTEL_JS;

        const $tpl = $("<div class='ts-code-intel-toast'>");
        $("<div class='ts-code-intel-msg'>").text(Strings.CODE_INTEL_ENABLED_MESSAGE).appendTo($tpl);
        const $btns = $("<div class='ts-code-intel-buttons'>").appendTo($tpl);
        const $see = _action(Strings.CODE_INTEL_SEE_CONFIG).appendTo($btns);
        let $enableTs = null;
        if (!isTs) {
            $enableTs = _action(Strings.CODE_INTEL_ENABLE_TS).appendTo($btns);
        }
        const $learn = _action(Strings.CODE_INTEL_LEARN_MORE).appendTo($btns);

        _trackToast(StringUtils.format(Strings.CODE_INTEL_ENABLED_TITLE, langName), $tpl);

        $see.on("click", function () {
            _openConfig();
        });
        if ($enableTs) {
            $enableTs.on("click", function () {
                (async function () {
                    // Flip checkJs on whichever config we manage (it may have been upgraded to
                    // tsconfig by now); fall back to jsconfig if none is on disk anymore.
                    const rootPath = _projectRootPath();
                    const managed = rootPath && await _readManagedConfig(rootPath);
                    const ok = await _writeConfig((managed && managed.fileName) || JS_CONFIG_FILE, true);
                    if (ok) {
                        _showEnabledToast(true); // relabel as TypeScript (also dismisses this toast)
                    }
                }());
            });
        }
        $learn.on("click", _learnMore);
    }

    // ----- Problems-panel re-enable row -----------------------------------------------------------
    // A banner inside #problems-panel offering to re-enable project-wide intelligence. Shown when a
    // JS/TS file is active in a project where Phoenix created a config before but it's now gone (the
    // user deleted it). It lives as a sibling of the results table, so CodeInspection re-rendering
    // the table doesn't wipe it.

    function _ensurePanelRow() {
        const $panel = $("#problems-panel");
        if (!$panel.length) {
            return null;
        }
        let $row = $panel.find(".ts-code-intel-panel-row");
        if ($row.length) {
            return $row;
        }
        $row = $("<div class='ts-code-intel-panel-row'>").hide();
        $("<span class='ts-code-intel-panel-text'>").text(Strings.CODE_INTEL_PANEL_TEXT).appendTo($row);
        $("<button class='btn btn-mini primary ts-code-intel-panel-enable'>")
            .text(Strings.CODE_INTEL_PANEL_ENABLE)
            .on("click", function () {
                promptEnable();
            })
            .appendTo($row);
        $("<a class='ts-code-intel-panel-close'>")
            .attr("title", Strings.CODE_INTEL_PANEL_DISMISS).html("&times;")
            .on("click", function () {
                const rootPath = _projectRootPath();
                if (rootPath) {
                    _panelRowClosed.add(rootPath); // session-only: reappears next launch
                }
                $row.hide();
            })
            .appendTo($row);
        $panel.children(".toolbar").after($row);
        return $row;
    }

    function _updatePanelRow() {
        if (typeof Phoenix !== "undefined" && Phoenix.isTestWindow) {
            return;
        }
        const $row = _ensurePanelRow();
        if (!$row) {
            return;
        }
        const rootPath = _projectRootPath();
        const editor = EditorManager.getActiveEditor();
        const lang = editor && editor.document && editor.document.getLanguage().getId();
        const supportedActive = lang && _options.supportedLanguages.indexOf(lang) !== -1;
        // Show only when a JS/TS file is active, Phoenix created a config here before (so its absence
        // means the user deleted it), and they didn't close the row this session.
        if (!rootPath || !supportedActive || !_isCreated() || _panelRowClosed.has(rootPath)) {
            $row.hide();
            return;
        }
        _hasProjectConfig().then(function (hasConfig) {
            if (_projectChangedSince(rootPath)) {
                return;
            }
            $row.toggle(!hasConfig);
        });
    }

    /**
     * On the first JS/TS file opened in a config-less, non-dismissed project (once per session),
     * silently create the config - tsconfig when the project contains TypeScript, else jsconfig.
     * Deliberately no toast: the generated file self-documents (autoGeneratedByPhoenixCode.doc) and
     * is visible in the file tree / source control.
     * @param {Editor} editor
     */
    async function _autoEnable(editor) {
        if (Phoenix.isTestWindow) {
            return; // never write configs from a test window - it would pollute fixtures
        }
        if (PreferencesManager.get(PREF_AUTO_CREATE) === false) {
            return; // the user opted out of auto-creation (managing existing configs is unaffected)
        }
        if (!editor || !editor.document) {
            return;
        }
        const lang = editor.document.getLanguage().getId();
        if (_options.supportedLanguages.indexOf(lang) === -1) {
            return;
        }
        const rootPath = _projectRootPath();
        if (!rootPath || _evaluated.has(rootPath)) {
            return;
        }
        _evaluated.add(rootPath);
        if (_isCreated()) {
            return; // we created a config here before; if it's gone the user deleted it - don't fight
        }
        const hasConfig = await _hasProjectConfig();
        if (_projectChangedSince(rootPath) || hasConfig) {
            return; // project switched mid-read, or already scoped - nothing to do
        }
        // The opened file being .ts/.tsx is itself proof of a TS project (skip the scan); otherwise
        // scan for a real .ts file elsewhere in the project.
        const hasTs = (TS_LANGUAGES.indexOf(lang) !== -1) || (await _projectHasTsFiles());
        if (_projectChangedSince(rootPath)) {
            return;
        }
        await _writeConfig(hasTs ? TS_CONFIG_FILE : JS_CONFIG_FILE, false);
    }

    // Project roots already checked for a jsconfig -> tsconfig upgrade this session.
    const _upgradeChecked = new Set();

    /**
     * Silently upgrade a Phoenix-managed jsconfig.json to tsconfig.json the first time a TypeScript
     * file becomes active in the project: TS projects are conventionally scoped by tsconfig (external
     * tooling reads only it), and the autoManage contract lets us do this without asking -
     * compilerOptions are preserved and the tsconfig carries allowJs + noEmit so it stays build-safe.
     * A hand-written tsconfig (or any non-managed jsconfig) means the user owns the setup: hands off.
     * @param {Editor} editor
     */
    async function _maybeUpgradeToTs(editor) {
        if (Phoenix.isTestWindow || !editor || !editor.document) {
            return;
        }
        if (TS_LANGUAGES.indexOf(editor.document.getLanguage().getId()) === -1) {
            return;
        }
        const rootPath = _projectRootPath();
        if (!rootPath || _upgradeChecked.has(rootPath)) {
            return;
        }
        _upgradeChecked.add(rootPath);
        // Never overwrite an existing tsconfig - if the user added their own, they own the setup.
        if (await _fileExists(rootPath + TS_CONFIG_FILE)) {
            return;
        }
        const managed = await _readManagedConfig(rootPath);
        if (_projectChangedSince(rootPath) || !managed || managed.fileName !== JS_CONFIG_FILE) {
            return; // no managed jsconfig here (none at all, or user-owned)
        }
        const ok = await _writeConfig(TS_CONFIG_FILE); // checkJs omitted: preserve the existing value
        if (!ok || _projectChangedSince(rootPath)) {
            return;
        }
        FileSystem.getFileForPath(rootPath + JS_CONFIG_FILE).unlink(function (err) {
            if (err) {
                console.error("[TypeScriptSupport] failed to remove " + JS_CONFIG_FILE + " after upgrade", err);
            }
        });
        console.log("[TypeScriptSupport] upgraded " + JS_CONFIG_FILE + " -> " + TS_CONFIG_FILE +
            " (TypeScript detected in project)");
    }

    /**
     * Public entry for the Problems-panel re-enable affordance: (re)create the config for the
     * current project and reuse the same enabled-toast flow as auto-enable. The label comes from the
     * active file's language (instant) rather than a full project scan, so the toast isn't delayed.
     */
    function promptEnable() {
        const editor = EditorManager.getActiveEditor();
        const lang = editor && editor.document && editor.document.getLanguage().getId();
        const isTs = TS_LANGUAGES.indexOf(lang) !== -1;
        _writeConfig(isTs ? TS_CONFIG_FILE : JS_CONFIG_FILE, false).then(function (ok) {
            if (!ok) {
                return;
            }
            _updatePanelRow(); // config exists again - hide the re-enable row
            _showEnabledToast(isTs);
        });
    }

    /**
     * @param {{supportedLanguages: Array<string>, restartServer: function}} options
     */
    function init(options) {
        _options = options || {};
        EditorManager.on("activeEditorChange.tsCodeIntel", function (evt, current) {
            _autoEnable(current);
            _maybeUpgradeToTs(current);
            _updatePanelRow(); // the row depends on the active file's language
        });
        // Also catch switches to non-editor views (image/preview) where activeEditorChange may not fire.
        MainViewManager.on("currentFileChange.tsCodeIntel", _updatePanelRow);
        // A pending toast belongs to the project that was open when it appeared - drop it on switch;
        // also refresh the Problems-panel re-enable row for the new project.
        ProjectManager.on(ProjectManager.EVENT_PROJECT_OPEN + ".tsCodeIntel", function () {
            _dismissActiveNotification();
            _updatePanelRow();
        });
        // Evaluate the file already showing at startup (the common "restored a JS file" case).
        _autoEnable(EditorManager.getActiveEditor());
        _maybeUpgradeToTs(EditorManager.getActiveEditor());
        _updatePanelRow();
    }

    exports.init = init;
    exports.promptEnable = promptEnable;
    exports.PREF_AUTO_CREATE = PREF_AUTO_CREATE;
    exports.DOCS_URL = DOCS_URL;
    // exposed for unit tests (pure function; the event-driven flow never runs in test windows)
    exports._configContent = _configContent;
});
